# Реалізація множення байта на 2 в полі Галуа
def mul02(byte):
    result = byte << 1  # зсув вліво на 1 біт
    if result & 0x100:  # якщо результат більший за 8 біт (перший біт 1), необхідно застосувати модульний поліном
        result ^= 0x1b  # застосування полінома m(x) = 0001 1011
    result &= 0xff  # обмеження результату до 8 біт
    return result


# Реалізація множення байта на довільне число в полі Галуа
def galois_multiply(a, b):
    result = 0
    while b > 0:
        if b & 1:  # коли найменший біт у b встановлений
            result ^= a  # додаємо a до результату
        a = mul02(a)  # множимо на 2 (зсуваємо вліво)
        b >>= 1  # зсуваємо b вправо на 1 біт
    return result


# Тестування на наведеному прикладі
def test():
    byte1 = 0x57
    byte2 = 0x83
    result = galois_multiply(byte1, byte2)
    print(f"57 * 83 = {result:02X}")  # Очікуваний результат C1


# Виклик функції тестування
test()